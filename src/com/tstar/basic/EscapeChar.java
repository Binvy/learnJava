package com.tstar.basic;

public class EscapeChar {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("=====Tab键=====");
		System.out.println(1 + "\t" + 2);
		System.out.println("=====退格键=====");
		System.out.println(1 + "\b" + 2);
		System.out.println("=====换行建=====");
		System.out.println(1 + "\n" + 2);
		System.out.println("=====换页符=====");
		System.out.println(1 + "\f" + 2);
		System.out.println("=====回车符=====");
		System.out.println(1 + "\r" + 2);
		System.out.println("=====反斜杠=====");
		System.out.println(1 + "\\" + 2);
		System.out.println("=====双引号=====");
		System.out.println(1 + "\"" + 2);
	}

}
